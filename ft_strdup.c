#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dup;
	unsigned long	len;
	unsigned long	i;

	len = ft_strlen(s);
	dup = malloc(len + 1);
	if (!dup)
		return (0);
	i = 0;
	while (i < len)
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
