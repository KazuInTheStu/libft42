#include "libft.h"

void	*ft_calloc(unsigned long nmemb, unsigned long size)
{
	size_t	i;
	char	*ptr;

	ptr = malloc(nmemb * size);
	if (!ptr)
		return (NULL);
	i = 0;
	while (i < nmemb * size)
	{
		ptr[i] = 0;
		i++;
	}
	return ((void *)ptr);
}
